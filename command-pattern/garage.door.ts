export class GarageDoor {
    up(): void {
        console.log("차고 문이 올라갔습니다.");
    }
    down(): void {
        console.log("차고 문이 내려갔습니다.");
    }
    stop(): void {
        console.log("차고 문이 멈췄습니다.");
    }
    lightOn(): void {
        console.log("차고 조명이 켜졌습니다.");
    }
    lightOff(): void {
        console.log("차고 조명이 꺼졌습니다.");
    }
}
